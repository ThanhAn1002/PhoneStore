    public void getTax(int t) {
        if (t > 100.0 && t <= 200.0) {
            tax = 5;
        } else if (t > 200.0 && t <= 400.0) {
            tax = 10;
        } else if (t > 400.0 && t <= 800.0) {
            tax = 15;
        } else if (t > 800.0 && t <= 1200.0) {
            tax = 20;
        } else if (t > 1200.0 && t <= 1600.0) {
            tax = 25;
        } else if (t > 1600.0 && t <= 2000.0) {
            tax = 30;
        } else if (t > 2000.0) {
            tax = 35;
        }
    }

