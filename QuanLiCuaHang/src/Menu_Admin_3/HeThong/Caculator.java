    private void button6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button6ActionPerformed

        try {
            String input = t1.getText();
            if (input.isEmpty()) {
                return;
            }
            if (operator == null) {
                num1 = Double.parseDouble(input);
                t1.setText(input);
                return;
            }
            num2 = Double.parseDouble(input);
            double ans = 0;
            switch (operator) {
                case "+":
                    ans = num1 + num2;
                    break;
                case "-":
                    ans = num1 - num2;
                    break;
                case "*":
                    ans = num1 * num2;
                    break;
                case "/":
                    if (num2 == 0) {
                        JOptionPane.showMessageDialog(null, "Division by zero error!");
                        return;
                    }
                    ans = num1 / num2;
                    break;
            }
            if (ans % 1.0 == 0) {
                int intAns = (int) ans;
                t1.setText("" + intAns);
            } else {
                t1.setText("" + ans);
            }

            operator = null;
            num1 = ans;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Syntax error. Please enter a number!");
        }
    }//GEN-LAST:event_button6ActionPerformed

